package javafirst;

public class switches {
  public static void main(String args[])
  {
	  int cat=2;
	  switch(cat){
	  case 1:
		  System.out.println("40%");break;
	  case 2:
		  System.out.println("30%");break;
	  case 3:
		  System.out.println("20%");break;
	  case 4:
		  System.out.println("10%");break;
		  default:
			  System.out.println("Enough");
		  
	  }
  }
}
